export async function seedDatabase() {
  const count = await storage.getLeadCount();
  if (count > 0) {
    console.log("Database already seeded, skipping...");
    return;
  }

  console.log("Seeding database...");

  for (const event of seedHailEvents) {
    await storage.createHailEvent(event);
  }

  for (const leadData of seedLeads) {
    const score = calculateScore(leadData as InsertLead);
    await storage.createLead({ ...leadData, leadScore: score } as InsertLead);
  }

  console.log(`Seeded ${seedLeads.length} leads and ${seedHailEvents.length} hail events`);
}
